    <h2>About</h2>
    <p>Copper Highway is a donation-funded, invite-only VPN service built from the <abbr title="Named for the open-ness of its source code, not its security!">OpenVPN</abbr> standard, an open-source VPN implementation that provides a much higher level of security than other protocols like PPTP or IPSec.</p>
    <h4>Key Features</h4>
    <h5>Cross-platform Support</h5>
    <p>OpenVPN is supported on Windows, OSX, *nix, iOS, and Android</p>
    <h5>Modern Cryptography</h5>
    <p>We are our own Certificate Authority (CA), a VPN industry-standard, we use only TLS 1.0+ cipher suites that support forward secrecy and never, <span class="italic">ever</span> employ outdated or broken cryptographic protocols.</p>
    <h5>No logs</h5>
    <p>We don't log your VPN activity or usage, ever.</p>
