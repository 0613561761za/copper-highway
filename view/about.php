    <h2>About</h2>
    <p>Copper Highway was born out of the frustration of paying high prices for VPN services that we didn't control and the realization that we could easily do it better for cheaper.  </p>
    <p>Copper Highway uses <abbr title="Named for the open-ness of its source code, not its security!">OpenVPN</abbr>, and open-source VPN implementation that provides a much higher level of security than other protocols like PPTP or IPSec. The service is built on leased Amazon Web Services (AWS) virtual private servers, which affords us <span class="italic">full control</span> of the infrastructure at a relatively low cost.</p>
    <h3>Key Features</h3>
    <h4>Cross-platform Support</h4>
    <p>OpenVPN is supported on Windows, OSX, Linux, BSD, iOS, and Android</p>
    <h4>Modern Cryptography</h4>
    <p>We are our own Certificate Authority (CA), a VPN industry-standard, we use only TLS 1.0+ cipher suites that support forward secrecy and never, <span class="italic">ever</span> employ outdated or broken cryptographic protocols.</p>
    <h4>No logs</h4>
    <p>We don't log your VPN activity or usage, ever.</p>
