    <h6>Requirements</h6>
    <p>
	<ul>
	    <li>Proficient with the Linux command line</li>
	    <li>Understands the basics of modern computer cryptography</li>
	    <li>Experience managing an OpenVPN server</li>
	    <li>Experience managing Nginx HTTPD</li>
	    <li>Experience with web server hardening</li>
	    <li>Experience managing a linux system</li>
	</ul>
    </p>
