    <p>Manages the website's backend to include database management, bug fixes, and improvements.</p>
    <h6>Requirements</h6>
    <p>
	<ul>
	    <li>Proficient in PHP and SQL</li>
	    <li>Working knowledge of Git</li>
	    <li>Comfortable with the Linux command line</li>
	</ul>
    </p>

