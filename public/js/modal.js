function closeModal() {
    document.getElementById("modal-blur").style.visibility= "hidden";
    document.getElementById("modal").style.visibility= "hidden";
    document.getElementById("modal-text").style.visibility= "hidden";
}

function openModal() {
    document.getElementById("modal-blur").style.visibility= "visible";
    document.getElementById("modal").style.visibility= "visible";
    document.getElementById("modal-text").style.visibility= "visible";
}
